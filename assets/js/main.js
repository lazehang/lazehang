const phrases = [
    'Laze Hang',
    'Mangal Hang',
    'Coder',
    'WebDeveloper',
    'React',
    'Node Js',
    'JavaScript',
    'Css3',
    'HTML5',
    'Php',
    'JQuery'
]

const el = document.querySelector('.text')
const fx = new TextScramble(el)
let counter = 0
const next = () => {
    fx.setText(phrases[counter]).then(() => {
        setTimeout(next, 1500)
    })
    counter = (counter + 1) % phrases.length
}

next();